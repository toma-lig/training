/* 
Cmd+F　ファイル内検索 
手順：sayHello8を検索してみる
*/
function sayHello8 (firstName: string) {
    console.log('Hello ' + firstName)
}



/* 
Cmd+Shift+F　横断検索
手順：別ファイルのsayHello5を検索してみる
 */



/* 
Cmd+P　ファイル検索
手順：別ファイルの「01_カーソル移動・選択」を検索してみる
 */


/* 
Cmd+T　シンボル名を横断検索 
手順：たくさんあるsayHelloを検索してみる
*/


/* 
Cmd+Option+F　ファイル内の文字列置換 
手順：sayHello9を検索し、sayHello99に置換してみる（関数名のみ置換。コメントのsayHello9はそのままにする）
*/
function sayHello9 (firstName: string) {
    console.log('Hello ' + firstName)
}



