/* 
Cmd+C　選択コピー
Cmd+V　ペースト 
Cmd+X　選択カット
手順：sayHello6を複製し、複製したsayHello6をカットで消す
*/
function sayHello6 (firstName: string) {
    console.log('Hello ' + firstName)
}



/* 
範囲選択しないでCmd+C　行コピー
範囲選択しないでCmd+X　行カット 
手順：console.logの行を行コピーして複製し、行カットで消す
*/
function sayHello７ (firstName: string) {
    console.log('Hello ' + firstName)
}



